vars = [int(i) for i in input().split(" ")]
heightImage = vars[0]
widthImage = vars[1]
heightKer = vars[2]
widthKer = vars[3]

imageArray = []*heightImage
kerArray = []**heightKer

for h in (heightImage):
    imageArray[h] = input()

for r in (heightKer):
    kerArray[r] = input()